-- Q9. What is the name and city of the salesperson who sold the most number of cars in the year 2023?	
	
-- Method 1	
	SELECT sp.name,sp.city,COUNT(s.car_id) AS total_car_sold
    FROM salespersons sp
	JOIN sales s ON sp.salesman_id = s.salesman_id
	JOIN cars c ON s.car_id = c.car_id
	WHERE EXTRACT (YEAR FROM s.purchase_date) = 2023
	GROUP BY sp.name,sp.city
	ORDER BY total_car_sold DESC
	LIMIT 1;
	
-- Method 2
	WITH ctel as ( SELECT salesman_id, car_id , EXTRACT(YEAR FROM sales.purchase_date) AS year_of_purchase FROM sales )
	SELECT sp.name,sp.city,COUNT(c.car_id) AS total_car_sold
    FROM salespersons sp
	JOIN ctel ON sp.salesman_id = ctel.salesman_id
	JOIN cars c ON c.car_id = ctel.car_id
	WHERE year_of_purchase = '2023'
	GROUP BY sp.name,sp.city
	ORDER BY total_car_sold DESC
	LIMIT 1;
	
	
