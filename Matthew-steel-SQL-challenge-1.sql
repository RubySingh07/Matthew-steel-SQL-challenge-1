-- Q7. What is the total revenue generated by the sales of hatchback cars?	
	
	SELECT cars.style,SUM(cars.cost_$) AS total_revenue
	FROM cars
	JOIN sales ON cars.car_id = sales.car_id
	WHERE cars.style = 'Hatchback'
	GROUP BY cars.style;
	
