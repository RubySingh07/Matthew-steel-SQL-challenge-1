-- Q1. What are the details of all cars purchased in the year 2022?
	SELECT * FROM cars
	JOIN sales ON  sales.car_id = cars.car_id
	WHERE EXTRACT(YEAR FROM purchase_date) = 2022
	ORDER BY purchase_date DESC;
-- Q2. What is the total number of cars sold by each salesperson?
	SELECT sp.salesman_id,sp.name ,COUNT(sale_id) AS cars_sold
	FROM sales s
	JOIN salespersons sp ON s.salesman_id = sp.salesman_id
	GROUP BY sp.salesman_id
	ORDER BY sp.salesman_id ;
	
	
-- Q3. What is the total revenue generated by each salesperson?	
	
	SELECT sp.salesman_id,sp.name,SUM(cost_$) AS total_revenue
	FROM salespersons sp
	INNER JOIN sales s ON sp.salesman_id = s.salesman_id
	INNER JOIN cars c ON s.car_id = c.car_id
	GROUP BY sp.salesman_id
	ORDER BY sp.salesman_id;
	
-- Q4. What are the details of the cars sold by each salesperson?	
	
	SELECT sp.salesman_id ,sp.name,c.car_id,c.make,c.type,c.style,c.cost_$
	FROM cars c
	INNER JOIN sales s ON s.car_id = c.car_id
	INNER JOIN salespersons sp On sp.salesman_id = s.salesman_id
	GROUP BY sp.salesman_id, c.car_id
	ORDER BY salesman_id;
	
-- Q5. What is the total revenue generated by each car type?
	
	SELECT c.type, SUM(cost_$) AS total_revenue_generated
	FROM cars c
	JOIN sales s ON c.car_id = s.car_id
	GROUP BY c.type;
	
